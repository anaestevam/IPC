#include <stdio.h>
float Soma( float a, float b );
float Subtracao( float a, float b );
float Multiplicacao( float a, float b );
float Divisao( float a, float b );



float Soma( float a, float b ){
        return(a+b);
}
float Subtracao( float a, float b ){
        return (a-b);
}
float Multiplicacao( float a, float b ){
        return (a*b);
}
float Divisao( float a, float b ){
        return (a/b);
}

